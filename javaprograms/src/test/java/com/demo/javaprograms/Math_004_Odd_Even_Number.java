public class Math_004_Odd_Even_Number {

	public static void main(String[] args) {
		int n=5;
			if(n%2==0) {
				System.out.println("Given Number "+n +" is: Even Number");
			}else
			{
				System.out.println("Given Number " +n +" is: Odd Number");
			}
	}
}
